#include<iostream>
using namespace std;
int main(){
    int x,y;
    cout<<"Enter the value of x: ";
    cin>>x;
    cout<<"Enter the value of y: ";
    cin>>y;
    cout<<"the sum of x and y is ";
    cout<<x+y;
    cout<<endl;
    cout<<"product of x and y is ";
    cout<<x*y;
    cout<<endl;
    cout<<"the substraction of x and y is ";
    cout<<x-y;
    cout<<endl;
    cout<<"the division of x and y is ";
    cout<<x/y;
    cout<<endl;
    cout<<"the remainder of x and y is ";
    cout<<x%y;
}